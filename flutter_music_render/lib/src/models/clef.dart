/// Represents a musical clef.
enum Clef {
  /// Treble clef (G clef)
  treble,

  /// Bass clef (F clef)
  bass,

  /// Alto clef (C clef)
  alto,

  /// Tenor clef (C clef)
  tenor,

  /// Soprano clef (C clef)
  soprano,

  /// Mezzo-soprano clef (C clef)
  mezzoSoprano,

  /// Baritone clef (F clef)
  baritone,

  /// Varbaritone clef (F clef)
  varbaritone,

  /// Subbass clef (F clef)
  subbass,

  /// French violin clef (G clef)
  french,

  /// Treble clef with 8va (G clef)
  treble8va,

  /// Treble clef with 8vb (G clef)
  treble8vb,

  /// Bass clef with 8va (F clef)
  bass8va,

  /// Bass clef with 8vb (F clef)
  bass8vb,

  /// Tab clef
  tab,

  /// Percussion clef
  percussion,
}
