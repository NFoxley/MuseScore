/// Represents a musical key
enum MusicalKey {
  c, // C major / A minor
  g, // G major / E minor
  d, // D major / B minor
  a, // A major / F# minor
  e, // E major / C# minor
  b, // B major / G# minor
  fs, // F# major / D# minor
  cs, // C# major / A# minor
  f, // F major / D minor
  bb, // Bb major / G minor
  eb, // Eb major / C minor
  ab, // Ab major / F minor
  db, // Db major / Bb minor
  gb, // Gb major / Eb minor
  cb, // Cb major / Ab minor
}
